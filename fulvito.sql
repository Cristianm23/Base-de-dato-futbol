CREATE DATABASE IF NOT EXISTS fulvito;
USE fulvito;

CREATE TABLE Equipos (
    Id_Equipos INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL,
    Ciudad VARCHAR(100) NOT NULL,
    Fundacion DATE NOT NULL,
    Estadio VARCHAR(100)
);

CREATE TABLE Jugadores (
    Id_Jugadores INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL,
    Apellido VARCHAR(100) NOT NULL,
    Edad INT NOT NULL, -- Cambiado a INT para una mejor representación
    NumeroJugador TEXT(100) NOT NULL, -- Cambiado a VARCHAR(10) para limitar longitud
    Altura DECIMAL(5,2), -- Cambiado a DECIMAL para mejor precisión
    Cedula VARCHAR(100) NOT NULL,
    FechaNacimiento DATE,
    Posicion VARCHAR(50),
    EquipoId INT,
    FOREIGN KEY (EquipoId) REFERENCES Equipos(Id_Equipos) -- Referencia corregida
);

CREATE TABLE Arbitros (
    Id_Arbitros INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL,
    Apellido VARCHAR(100) NOT NULL,
    Edad INT NOT NULL, -- Cambiado a INT para una mejor representación
    Cedula VARCHAR(100) NOT NULL,
    Nacionalidad VARCHAR(50),
    Experiencia INT -- años de experiencia
);

CREATE TABLE Partidos (
    Id_Partidos INT AUTO_INCREMENT PRIMARY KEY,
    EquipoLocalId INT,
    EquipoVisitanteId INT,
    Fecha DATE,
    ArbitroId INT,
    Resultado VARCHAR(20),
    FOREIGN KEY (EquipoLocalId) REFERENCES Equipos(Id_Equipos), -- Referencia corregida
    FOREIGN KEY (EquipoVisitanteId) REFERENCES Equipos(Id_Equipos), -- Referencia corregida
    FOREIGN KEY (ArbitroId) REFERENCES Arbitros(Id_Arbitros) -- Referencia corregida
);

CREATE TABLE Plantillas (
    Id_Plantillas INT AUTO_INCREMENT PRIMARY KEY,
    EquipoId INT,
    JugadorId INT,
    Posicion VARCHAR(50),
    FOREIGN KEY (EquipoId) REFERENCES Equipos(Id_Equipos), -- Referencia corregida
    FOREIGN KEY (JugadorId) REFERENCES Jugadores(Id_Jugadores) -- Referencia corregida
);

